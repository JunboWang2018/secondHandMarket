package cn.showclear.www.pojo.base;

import java.sql.Timestamp;

/**
 * @author Wang Junbo
 * @date 2019/4/8
 * 商品类，对应数据库商品表T_PRODUCT
 */
public class ProductDo {
    private Integer productId;
    private String productNumber;
    private String name;
    private String description;
    private String depreciationRate;    //折旧率
    private Integer quantity;           //数量
    private String image;
    private Double price;
    private Integer isSold;
    private Integer typeCode;           //商品类型
    private Integer saleWayCode;        //出售形式
    private Integer userId;
    private Timestamp createDate;
    private Timestamp modifyDate;

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public String getProductNumber() {
        return productNumber;
    }

    public void setProductNumber(String productNumber) {
        this.productNumber = productNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDepreciationRate() {
        return depreciationRate;
    }

    public void setDepreciationRate(String depreciationRate) {
        this.depreciationRate = depreciationRate;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Integer getIsSold() {
        return isSold;
    }

    public void setIsSold(Integer isSold) {
        this.isSold = isSold;
    }

    public Integer getTypeCode() {
        return typeCode;
    }

    public void setTypeCode(Integer typeCode) {
        this.typeCode = typeCode;
    }

    public Integer getSaleWayCode() {
        return saleWayCode;
    }

    public void setSaleWayCode(Integer saleWayCode) {
        this.saleWayCode = saleWayCode;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Timestamp getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Timestamp createDate) {
        this.createDate = createDate;
    }

    public Timestamp getModifyDate() {
        return modifyDate;
    }

    public void setModifyDate(Timestamp modifyDate) {
        this.modifyDate = modifyDate;
    }

    @Override
    public String toString() {
        return "ProductDo{" +
                "productId=" + productId +
                ", productNumber='" + productNumber + '\'' +
                ", name='" + name + '\'' +
                ", description='" + description + '\'' +
                ", depreciationRate='" + depreciationRate + '\'' +
                ", quantity=" + quantity +
                ", image='" + image + '\'' +
                ", price=" + price +
                ", isSold=" + isSold +
                ", typeCode=" + typeCode +
                ", saleWayCode=" + saleWayCode +
                ", userId=" + userId +
                ", createDate=" + createDate +
                ", modifyDate=" + modifyDate +
                '}';
    }
}
